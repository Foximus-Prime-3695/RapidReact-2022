package frc.robot.commands.intake;

import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.SubsystemIntake;

public class CyborgCommandEnablePrimaryIntake extends CommandBase {
  SubsystemIntake intake;
  /** Creates a new CyborgCommandEnablePrimaryIntake. */
  public CyborgCommandEnablePrimaryIntake(SubsystemIntake intake) {
    this.intake = intake;
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {
  }

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    intake.setPrimaryIntake(0.67D);
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {
  }

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
